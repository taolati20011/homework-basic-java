package exercise.B15;

public class Result {
    String year;
    double avg;

    public Result(String year, double avg) {
        this.year = year;
        this.avg = avg;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public double getAvg() {
        return avg;
    }

    public void setAvg(double avg) {
        this.avg = avg;
    }
}
